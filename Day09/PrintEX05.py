print("hello world".capitalize()) #문자의 첫 글자를 대문자로 변경
print("hello world".upper())  # 모든 문자를 대문자로 변경
print("hello world".lower())  # 모든 문자를 소문자로 변경
print(len("hello world"))# 문자의 길이
print("hello world".replace("world" , "Python"))

