print(math.ceil(2.24)) # ceil 사전적 의미는 천정 , 소수점 이하를 올림역활을한다.
print(math.floor(2.7)) # floor 사전적 의미는 바닥으로 ,소수점 이하를 버리는 역활을한다.
print(math.pow(2, 10)) # 2의10승 , 수의 제곱을 수행한다.
print(math.pi)         # 파이값
print(math.ceil(math.pi))